# 摄氏度与华氏度对照表
# 从摄氏温度0度到250度，每隔20度为一项，不超过10条

print("摄氏度(°C)    华氏度(°F)")
print("------------------------")

celsius = 0  # 起始摄氏温度
count = 0    # 计数器，记录条目数

while count < 10 and celsius <= 250:
    # 计算华氏温度
    fahrenheit = celsius * 9 / 5.0 + 32
    # 输出对照表条目
    print(f"{celsius:^10.0f}    {fahrenheit:^10.1f}")
    # 更新摄氏温度和计数器
    celsius += 20
    count += 1
    #| 部分        | 含义                                    |
# | --------- | ------------------------------------- |
# | `celsius` | 要格式化的变量                               |
# | `:`       | 引出格式说明                                |
# | `^`       | 居中对齐（center align）                    |
# | `10`      | 总宽度为 10 个字符                           |
# | `.0f`     | 格式化为**浮点数（fixed-point）**，保留 **0 位小数** |
